// Animated close button
function openNav(x) {
    x.classList.toggle("change")
}

// Set width of sidenav to 250px
function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
}

// Set width of sidenav to 0
function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
}